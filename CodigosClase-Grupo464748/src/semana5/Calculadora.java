
package semana5;


public class Calculadora {
    
    public double getSuma(double a, double b){
        return a+b;
    }
    
    public double getResta(double a, double b){
        return a-b;
    }
    
    public double getDivision(double a, double b){
        return a/b;
    }
    
    public double getMultiplicacion(double a, double b){
        return a*b;
    }
    
}
